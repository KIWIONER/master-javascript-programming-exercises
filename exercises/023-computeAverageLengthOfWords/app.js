function computeAverageLengthOfWords (str1, str2){
    return (str1.length + str2.length) / 2;
}
console.log(computeAverageLengthOfWords("code", "programs")) // 6.5 
console.log(computeAverageLengthOfWords("you", "are")) // 3.5
console.log(computeAverageLengthOfWords("coding", "is")) // 4.5